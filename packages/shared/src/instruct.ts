import { z } from 'zod';

export const InstructTemplateSchema = z.object({
  name: z.string().min(1),
  systemSequence: z.string().default(''),
  systemPrefix: z.string().default(''),
  systemSuffix: z.string().default('\n'),
  inputSequence: z.string().default(''),
  inputSuffix: z.string().default('\n'),
  outputSequence: z.string().default(''),
  outputSuffix: z.string().default('\n'),
  firstOutputSequence: z.string().default(''),
  lastOutputSequence: z.string().default(''),
  firstInputSequence: z.string().default(''),
  lastInputSequence: z.string().default(''),
  stopSequence: z.string().default(''),
  systemSameAsUser: z.boolean().default(false),
  wrap: z.boolean().default(true),
  macro: z.boolean().default(true),
  namesBehavior: z.enum(['force', 'none', 'sample']).default('none'),
  userAlignmentMessage: z.string().default(''),
  storyStringTemplate: z.string().default(''),
  storyStringPrefix: z.string().default(''),
  storyStringSuffix: z.string().default(''),
  exampleSeparator: z.string().default(''),
  chatStart: z.string().default(''),
  activationRegex: z.string().default(''),
});

export type InstructTemplate = z.infer<typeof InstructTemplateSchema>;

