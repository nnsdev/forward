export function normalizeSillyTavernInstruct(raw: Record<string, unknown>): InstructTemplate {
  return InstructTemplateSchema.parse({
    activationRegex: String(raw.activation_regex ?? ''),
    chatStart: '',
    exampleSeparator: '',
    firstInputSequence: String(raw.first_input_sequence ?? ''),
    firstOutputSequence: String(raw.first_output_sequence ?? ''),
    inputSequence: String(raw.input_sequence ?? raw.inputSequence ?? ''),
    inputSuffix: String(raw.input_suffix ?? ''),
    lastInputSequence: String(raw.last_input_sequence ?? ''),
    lastOutputSequence: String(raw.last_output_sequence ?? ''),
    macro: Boolean(raw.macro ?? true),
    name: String(raw.name ?? 'Imported'),
    namesBehavior: String(raw.names_behavior ?? raw.namesBehavior ?? 'none'),
    outputSequence: String(raw.output_sequence ?? raw.outputSequence ?? ''),
    outputSuffix: String(raw.output_suffix ?? ''),
    stopSequence: String(raw.stop_sequence ?? ''),
    storyStringTemplate: DEFAULT_STORY_STRING_TEMPLATE,
    storyStringPrefix: String(raw.story_string_prefix ?? ''),
    storyStringSuffix: String(raw.story_string_suffix ?? ''),
    systemSameAsUser: Boolean(raw.system_same_as_user ?? false),
    systemPrefix: String(raw.system_sequence ?? raw.systemPrefix ?? ''),
    systemSequence: String(raw.system_sequence ?? ''),
    systemSuffix: String(raw.system_suffix ?? ''),
    userAlignmentMessage: String(raw.user_alignment_message ?? ''),
    wrap: Boolean(raw.wrap ?? true),
  });
}

export function normalizeSillyTavernContext(raw: Record<string, unknown>): Pick<InstructTemplate, 'chatStart' | 'exampleSeparator' | 'name' | 'storyStringTemplate'> {
  return {
    chatStart: String(raw.chat_start ?? ''),
    exampleSeparator: String(raw.example_separator ?? ''),
    name: String(raw.name ?? 'Imported'),
    storyStringTemplate: String(raw.story_string ?? DEFAULT_STORY_STRING_TEMPLATE),
  };
}
